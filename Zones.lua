--[[

Quest Map 2
	by Drakanwulf & Hawkeye1889

	General information about the Zones tables:
		Floors can be nested within dungeons and other objects like the Elden Root.  Dungeons can be nested within zones or 
		subzones. Subzones can be nested within other subzones or zones, and zones can share the same name with maps, except
		for Tamriel because that map has no equivalent zone.
		
		Each Zone Identifier (eg. zoneId) is unique to a specific zone.  In other words, every zoneId is assigned to a 
		specific area or tile that is located at specific x,y coordinates within the World Map.

  		Each Zone Name (eg. zoneName) is unique and it identifies a specific zone or subzone which allows the zoneName to be
		translated into any of the languages this addon supports via its $LANG files.  However, while each zoneName is unique
		to a specific zone or subzone, the same zoneName can be nested within one or more parent zones in a hierarchy of floors,
		subzones, zones, or maps.

		When you lookup a zone's name or a zone's x,y coordinate information by zoneId, you get the unique name assigned to
		that zone or you get the unique, global x,y coordinates of all the quest givers who exist within that zone.

		Looking up Zone Identifier information by zoneName has been depreciated because one zoneName search can return a list
		of one to many zoneId's with no information to differentiate among them.  This can make things difficult unless you 
		already knew the identifier for the specific zone, subzone, or map where the quest took place.  And if you already
		knew that, why waste CPU cycles to look it up again?
		
	There are three Zones Cross-Reference Tables
		Zone Names by Zone Identifier (zoneId)
			ZoneIds are unique but a zoneName can be be repeated because the same quest can be started from different maps,
			zones, or subzones.
*		Zone Identifiers by Zone Name (zoneName)
			zoneNames are unique but multiple zoneIds can be assigned to the same zoneName because the same quest can be
			started by different quest givers from different maps, zones, or subzones.
		Global Zone Location Information by Zone Identifier (zoneId)
			The LibGPS2 global location x,y coordinates for all of the known zones in the entire game are recorded here along 
			with their mapId's and zoneId's.  Magical mathematics known only to Votan are used to convert the local(to a map or
			zone) x,y coordinates to their global counterparts and back again.
			(See the LibGPS2 "LocalToGlobal" and "GlobalToLocal" API documentation for more information).

	API and Private functions are used to Add..., Get..., Set..., Update..., and Validate... table information
		API functions are public.  Use of the "QM2:" prefix exports these functions to the QM2 globals table as ESO loads the
		addon's compile units.
		Private functions are not for general use.  These functions are embedded within their respective tables so they cannot be
		seen outside of their respective compile units.
		(See the QM2.txt and Init.lua files for more information)

*	= A depreciated table or function because its values (table structures) or return values make it ambiguous to use.		

--]]

