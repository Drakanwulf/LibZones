		if type( zid ) ~= "number" or zid < 1 or zid > 2147483647 then		-- (2**32)-1 is 2147483647
			return false
		end

		return true
	end,

	ValidInfo = function( zname )
		if type( zname ) ~= "string" or zname = nil or zname == "" then	-- Empty zone names are not allowed
			return false
		end
		
		return true
	end,
}

function QM2:AddZoneName( zid, zname )
	-- Validate parameters
	if (not ids.ValidInfo( zid, zname ) then
		return false
	end

	-- And the name of this zone cannot be in the table
	if (ids[zid] ~= nil) then
		return false
	end

	return ids.SetName( zid, zname )
end

function QM2:GetZoneName( zid )
	return ids.GetInfo( zid )
end

function QM2:UpdateZoneName( zid, zname )
	--Zone Id must be a number and must be in the table
	if (type( zid ) ~= "number" or names[zid] == nil) then
		return false
	end

	if (not names.ValidInfo( zname )) then
		return false
	end	

	return names.SetInfo( zid, zname )
end



