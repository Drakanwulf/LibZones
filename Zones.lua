	DeleteInfo = function( zid )
		if not ValidId( zid ) or ids[zid] == nil		-- Check for a bad zoneId; ie. one that does not have a table entry
			return false
		end

		ids[zid] = nil									-- Delete the table entry and its zone identifier
		
		return true
