	GetInfo = function( zid )
		if not ValidId( zid ) or ids[zid] == nil		-- Check for a bad zoneId; ie. one that does not have a table entry
			return false
		end

		return ids[zid]									-- Return the zone name
	end,
	
	SetInfo = function( zid, zname )
		if not ValidId( zid ) or not ValidInfo( zname ) then
			return false
		end	

		-- This should work for both inserts and updates (I think?)
		ids[zid] = zname
	
	-- For testing only.  Comment out or delete when Adds and Updates are proven to work.
		if (ids[zid] ~= zname) then
			return false
		end
		
		return true
	end,

